#!/usr/bin/env node
/**
 * ╔══════════════════════════════════════════════════════════╗
 * ║   CODEBASE SNAPSHOT AUTO-UPDATER                        ║
 * ║   Updates codebase-snapshot.xml automatically           ║
 * ║                                                          ║
 * ║   Usage:                                                 ║
 * ║     node scripts/update-snapshot.js           # update  ║
 * ║     node scripts/update-snapshot.js --install-hook      ║
 * ║     node scripts/update-snapshot.js --dry-run           ║
 * ╚══════════════════════════════════════════════════════════╝
 */

const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

// ── CONFIG ────────────────────────────────────────────────────────────────────
const ROOT = path.resolve(__dirname, '..');
const SNAPSHOT = path.join(ROOT, 'codebase-snapshot.xml');
const SCHEMA   = path.join(ROOT, 'backend', 'prisma', 'schema.prisma');
const HOOK_PATH = path.join(ROOT, '.git', 'hooks', 'post-commit');
const DRY_RUN  = process.argv.includes('--dry-run');
const INSTALL_HOOK = process.argv.includes('--install-hook');

// ── HELPERS ───────────────────────────────────────────────────────────────────
function run(cmd, cwd = ROOT) {
  try {
    return execSync(cmd, { cwd, encoding: 'utf8' }).trim();
  } catch {
    return '';
  }
}

function xmlEscape(str) {
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&apos;');
}

function log(msg) {
  console.log(`[update-snapshot] ${msg}`);
}

// ── INSTALL GIT HOOK ──────────────────────────────────────────────────────────
if (INSTALL_HOOK) {
  const hookContent = `#!/bin/sh
# Auto-generated by update-snapshot.js --install-hook
# Runs codebase snapshot updater after every commit
echo "[git-hook] Updating codebase-snapshot.xml..."
node "$(git rev-parse --show-toplevel)/scripts/update-snapshot.js"
git add codebase-snapshot.xml 2>/dev/null || true
echo "[git-hook] Snapshot updated."
`;
  try {
    const gitDir = path.join(ROOT, '.git');
    if (!fs.existsSync(gitDir)) {
      console.error('[update-snapshot] ERROR: .git directory not found. Are you in a git repo?');
      process.exit(1);
    }
    const hooksDir = path.join(gitDir, 'hooks');
    if (!fs.existsSync(hooksDir)) fs.mkdirSync(hooksDir, { recursive: true });
    fs.writeFileSync(HOOK_PATH, hookContent, { mode: 0o755 });
    log(`✅ Git post-commit hook installed at: ${HOOK_PATH}`);
    log('   The snapshot will now auto-update after every git commit.');
  } catch (err) {
    console.error('[update-snapshot] ERROR installing hook:', err.message);
    process.exit(1);
  }
  process.exit(0);
}

// ── GATHER DATA ───────────────────────────────────────────────────────────────

// 1. Timestamp
const now = new Date().toISOString();

// 2. Git hash
const hash = run('git rev-parse --short HEAD') || 'unknown';

// 3. Recent git log (last 20 commits)
function getRecentChanges() {
  const logRaw = run(
    'git log --pretty=format:"%ad|||%H|||%s|||%an" --date=format:"%Y-%m-%d" -20'
  );
  if (!logRaw) return '  <!-- No git history found -->';

  const entries = [];
  for (const line of logRaw.split('\n')) {
    if (!line.trim()) continue;
    const parts = line.split('|||');
    if (parts.length < 4) continue;
    const [date, commitHash, subject, author] = parts;

    // Get files changed in this commit
    const filesRaw = run(`git diff-tree --no-commit-id -r --name-only ${commitHash}`);
    const files = filesRaw
      ? filesRaw.split('\n').filter(Boolean).slice(0, 8).join(', ')
      : 'unknown';

    entries.push(
      `  <change date="${xmlEscape(date)}" files="${xmlEscape(files)}" author="${xmlEscape(author)}">\n` +
      `    ${xmlEscape(subject)}\n` +
      `  </change>`
    );
  }
  return entries.join('\n');
}

// 4. Parse Prisma schema for model names
function getPrismaModels() {
  if (!fs.existsSync(SCHEMA)) return [];
  const content = fs.readFileSync(SCHEMA, 'utf8');
  const modelRegex = /^model\s+(\w+)\s*\{/gm;
  const models = [];
  let m;
  while ((m = modelRegex.exec(content)) !== null) {
    models.push(m[1]);
  }
  return models;
}

// 5. Scan directory structure for new files (top-level modules)
function getDirectoryStats() {
  const stats = { backend: 0, frontend: 0, monitoring: 0, scripts: 0 };
  for (const key of Object.keys(stats)) {
    const dir = path.join(ROOT, key);
    if (fs.existsSync(dir)) {
      try {
        const count = run(`find "${dir}" -type f | wc -l`);
        stats[key] = parseInt(count) || 0;
      } catch {
        stats[key] = 0;
      }
    }
  }
  return stats;
}

// 6. Detect new/removed source files vs last snapshot
function detectNewFiles() {
  if (!fs.existsSync(SNAPSHOT)) return { added: [], removed: [] };
  const snapshotContent = fs.readFileSync(SNAPSHOT, 'utf8');

  // Get current backend services
  const servicesDir = path.join(ROOT, 'backend', 'src', 'services');
  const routesDir   = path.join(ROOT, 'backend', 'src', 'routes');
  const pagesDir    = path.join(ROOT, 'frontend', 'src', 'pages');

  const currentServices = fs.existsSync(servicesDir) 
    ? fs.readdirSync(servicesDir).filter(f => f.endsWith('.ts'))
    : [];
  const currentRoutes = fs.existsSync(routesDir)
    ? fs.readdirSync(routesDir).filter(f => f.endsWith('.ts'))
    : [];
  const currentPages = fs.existsSync(pagesDir)
    ? fs.readdirSync(pagesDir).filter(f => f.endsWith('.tsx') || f.endsWith('.ts'))
    : [];

  const warnings = [];

  // Check for services not mentioned in snapshot
  for (const svc of currentServices) {
    if (!snapshotContent.includes(svc)) {
      warnings.push(`NEW file not in snapshot: backend/src/services/${svc}`);
    }
  }
  for (const r of currentRoutes) {
    if (!snapshotContent.includes(r)) {
      warnings.push(`NEW file not in snapshot: backend/src/routes/${r}`);
    }
  }
  for (const p of currentPages) {
    if (!snapshotContent.includes(p)) {
      warnings.push(`NEW file not in snapshot: frontend/src/pages/${p}`);
    }
  }

  return warnings;
}

// ── BUILD UPDATED SNAPSHOT ────────────────────────────────────────────────────
function updateSnapshot() {
  if (!fs.existsSync(SNAPSHOT)) {
    log('ERROR: codebase-snapshot.xml not found. Cannot update.');
    process.exit(1);
  }

  let content = fs.readFileSync(SNAPSHOT, 'utf8');

  // 1. Update timestamp
  content = content.replace(
    /Last Updated\s*:\s*.+/,
    `Last Updated  : ${now}`
  );

  // 2. Update snapshot hash
  content = content.replace(
    /Snapshot Hash\s*:\s*.*/,
    `Snapshot Hash : ${hash}`
  );

  // 3. Replace changes-log section
  const newChangesLog =
    `<changes-log>\n` +
    `  <!-- AUTO-GENERATED by scripts/update-snapshot.js on ${now} -->\n` +
    `  <!-- Git HEAD: ${hash} -->\n` +
    `  <!-- Format: [DATE] [FILES_CHANGED] [AUTHOR] [SUMMARY] -->\n\n` +
    getRecentChanges() + '\n\n' +
    `</changes-log>`;

  content = content.replace(
    /<changes-log>[\s\S]*?<\/changes-log>/,
    newChangesLog
  );

  // 4. Check for new files and log warnings
  const warnings = detectNewFiles();
  if (warnings.length > 0) {
    log('\n⚠️  WARNING: New files detected that are not in the snapshot!');
    log('   Update the relevant sections manually:');
    for (const w of warnings) log(`   → ${w}`);
    log('');
  }

  // 5. Get prisma model count for info
  const models = getPrismaModels();
  log(`📊 Prisma models detected: ${models.join(', ')}`);

  // 6. Directory stats
  const stats = getDirectoryStats();
  log(`📁 File counts — backend: ${stats.backend}, frontend: ${stats.frontend}, monitoring: ${stats.monitoring}`);

  if (DRY_RUN) {
    log('\n[DRY RUN] Changes that would be written:');
    log(`  - Last Updated: ${now}`);
    log(`  - Snapshot Hash: ${hash}`);
    log('  - changes-log section regenerated from git log');
    log('\nNo files were modified (--dry-run mode).');
    return;
  }

  fs.writeFileSync(SNAPSHOT, content, 'utf8');
  log(`✅ codebase-snapshot.xml updated successfully.`);
  log(`   Timestamp: ${now}`);
  log(`   Git HEAD:  ${hash}`);
}

// ── ENTRYPOINT ────────────────────────────────────────────────────────────────
log('Starting codebase snapshot update...');
try {
  updateSnapshot();
} catch (err) {
  console.error('[update-snapshot] FATAL ERROR:', err.message);
  process.exit(1);
}
